class Organization {
  final String logo;
  final String name;
  final String acronym;
  final String creationDate;
  final String sector;
  final String province;
  final String city;
  final String phone;
  final String email;

  Organization({
    required this.logo,
    required this.name,
    required this.acronym,
    required this.creationDate,
    required this.sector,
    required this.province,
    required this.city,
    required this.phone,
    required this.email,
  });
}
